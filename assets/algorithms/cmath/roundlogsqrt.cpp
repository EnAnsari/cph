#include<iostream>
#include<cmath>
using namespace std;

int main()
{
   cout << "Square root of 81 is: "; 
   cout << sqrt(81) << "\n";

   cout << "Rounding 2.6 to nearest integer gives: "; 
   cout << round(2.6) << "\n";

   cout << "Natural log of 2 is: ";
   cout << log(2) << "\n";

   return 0;
}
